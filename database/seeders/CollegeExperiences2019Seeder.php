    public function run(): void
    {
        DB::transaction(function () {
            $inserted = 0;
            $createdCompanies = 0;

            foreach ($this->rows() as $row) {
                $company = Company::firstOrCreate(
                    ['name_normalized' => Arabic::normalize($row['company'])],
                    ['name' => $row['company'], 'status' => 'approved'],
                );

                if ($company->wasRecentlyCreated) {
                    $createdCompanies++;
                }

                $fingerprint = mb_substr($row['review_text'], 0, 120);

                // Drop any prior copy of this exact review so the seeder can
                // be re-run safely without producing duplicates.
                DB::table('ratings')
                    ->where('company_id', $company->id)
                    ->where('review_text', 'like', $fingerprint.'%')
                    ->delete();

                DB::table('ratings')->insert([
                    'company_id' => $company->id,
                    'role_title' => $row['role_title'],
                    'department' => $row['department'] ?? null,
                    'city' => $row['city'] ?? null,
                    'duration_months' => $row['duration_months'],
                    'sector' => $row['sector'] ?? null,
                    'modality' => $row['modality'],
                    'stipend_sar' => $row['stipend_sar'] ?? null,
                    'had_supervisor' => $row['had_supervisor'] ?? null,
                    'mixed_env' => $row['mixed_env'] ?? null,
                    'job_offer' => $row['job_offer'] ?? null,
                    'rating_mentorship' => $row['rating_mentorship'],
                    'rating_learning' => $row['rating_learning'],
                    'rating_culture' => $row['rating_culture'],
                    'rating_compensation' => $row['rating_compensation'],
                    'overall_rating' => $row['overall_rating'],
                    'recommendation' => $row['recommendation'],
                    'review_text' => $row['review_text'],
                    'pros' => $row['pros'] ?? null,
                    'cons' => $row['cons'] ?? null,
                    'reviewer_name' => null,
                    'reviewer_major' => null,
                    'created_at' => now(),
                    'updated_at' => now(),
                ]);

                $inserted++;
            }

            $this->command?->info("CollegeExperiences2019Seeder: inserted {$inserted} ratings, created {$createdCompanies} new companies.");
        });
    }

