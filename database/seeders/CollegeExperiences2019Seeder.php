 * SOURCE  = 'college_2019'
 *
 * Curation rules applied manually to each row:
 *  - SKIP the 2 placeholder rows at the top.
 *  - SKIP junk rows: empty / single-dot / single-letter companies or
 *    reviews < 20 real characters (~34 rows).
 *  - SKIP rows with no explicit modality signal (the free-form text
 *    must mention حضوري / دوام / مقر / city office / عن بعد / online /
 *    هجين etc.).  Guessing modality is forbidden per the spec.
 *  - Ratings (1..5) are inferred from review tone — the form has no
 *    numeric column, and the spec explicitly allows this.
 *  - mixed_env / job_offer / stipend_sar come directly from form columns
 *    and are null when the responder wrote "لا يوجد" / empty.
 *  - duration_months parsed from the free-form "مدة التدريب" column.
 *  - recommendation derived from overall_rating (>=4 yes, =3 maybe, <=2 no).
 *
 * Idempotency:
 *  This seeder is NOT fully idempotent (re-runs may create extra rating rows
 *  if migrations were not refreshed).  To stay safe on re-run we delete any
 *  existing rating that matches (company_id, first 120 chars of review_text)
 *  before re-inserting this seeder's rows.  For a clean slate, prefer
 *  `php artisan migrate:fresh --seed`.
 */
class CollegeExperiences2019Seeder extends Seeder
